def dict_to_ordered_list(dictionary):
    # Sort the dictionary by value (frequency), in descending order, and return the keys
    return [key for key, _ in sorted(dictionary.items(), key=lambda item: item[1], reverse=True)]

def generate_next_pairs(text):
    # dict for {bigram: {next_bigram: count}}
    next_pair_counts = defaultdict(lambda: defaultdict(int))
    
    # Iterate through the text to collect bigram and next bigram frequencies
    for i in range(len(text) - 3):
        current_bigram = text[i:i+2]
        next_bigram = text[i+2:i+4]
        # 
        next_pair_counts[current_bigram][next_bigram] += 1
    
    # sord by descending freq
    sorted_next_pairs = {
        bigram: sorted(following_bigrams.items(), key=lambda x: x[1], reverse=True)
        for bigram, following_bigrams in next_pair_counts.items()
    }
    
    return sorted_next_pairs

