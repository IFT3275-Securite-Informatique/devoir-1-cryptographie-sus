def decoding_key(freq_ref, freq_num):
    # Create an empty dictionary to store the mappings
    dictionary = {}
    
    # Ensure that the lengths of both lists are equal
    if len(freq_ref) < len(freq_num):
        raise ValueError("The lengths of freq_ref and freq_num must be the same")
    
    # Map frequencies in freq_num to those in freq_ref
    for i in range(len(freq_num)):
        dictionary[freq_num[i]] = freq_ref[i]
    return dictionary

def find_nth_occurrence(string, substring, n):
    position = -1
    for _ in range(n):
        position = string.find(substring, position + 1)
        if position == -1:
            return -1  
    return position



def generate_pair_frequencies(corpus):
    pair_frequencies = Counter(cut_string_into_pairs(corpus.lower()))
    # filter out invalid pairs
    filtered_frequencies = {pair: freq for pair, freq in pair_frequencies.items() 
                        if all(97 <= ord(char) <= 122 or 65 <= ord(char) <= 90 or ord(char) in [32,44,39,46,224,233,232] for char in pair)}
    
    return filtered_frequencies

